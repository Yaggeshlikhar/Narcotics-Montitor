    private void btnAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppointmentActionPerformed
        // TODO add your handling code here:
        CitizenWorkRequest citizenWorkRequest = new CitizenWorkRequest();
        citizenWorkRequest.setSender(account);
        citizenWorkRequest.setStatus("Requested");

        Organization org = null;
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization o : e.getOrganizationDirectory().getOrganizationList()) {
                    if (o instanceof LocalRehabCentreOrganization) {
                        citizenWorkRequest.setStatus("Requested");
                        org = o;
                    }
                }
            }
        }
        if (org != null) {
            System.out.println("check");
            org.getWorkQueue().getWorkRequestList().add(citizenWorkRequest);
            account.getWorkQueue().getWorkRequestList().add(citizenWorkRequest);
        }
        JOptionPane.showMessageDialog(null, "Requested for Appointment");


    }//GEN-LAST:event_btnAppointmentActionPerformed

