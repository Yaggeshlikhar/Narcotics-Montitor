    public void populateRequestAppointment(ArrayList<Employee> emp) {
        DefaultTableModel model = (DefaultTableModel) tblRequestAppointment.getModel();
        model.setRowCount(0);

        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[3];
            row[0] = request.getAppointment();
            row[1] = request.getReceiver();
            row[2] = request.getMessage();
            model.addRow(row);
        }
    }

